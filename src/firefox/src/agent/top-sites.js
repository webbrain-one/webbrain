/**
 * True if hostname falls under a top-site registrable domain. Walks dotted
 * suffixes (docs.google.com -> google.com) so subdomains match, but never
 * matches a bare TLD and never matches attacker domains that merely embed a
 * top name (google.com.evil.io -> walks to evil.io, no match).
 */
export function isTopSite(hostname) {
  if (!hostname) return false;
  const host = String(hostname).toLowerCase().replace(/^www\./, "");
  const labels = host.split(".").filter(Boolean);
  for (let i = 0; i < labels.length - 1; i++) {
    if (TOP_SITES.has(labels.slice(i).join("."))) return true;
  }
  return false;
}
